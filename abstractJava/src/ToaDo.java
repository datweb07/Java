public class ToaDo {
    private int x;
    private int y;

    public ToaDo(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX(){
        return this.x;
    }

    public int setX(int x){
        return this.x = x;
    }

    public int getY(){
        return this.y;
    }

    public int setY(int y){
        return this.y = y;
    }
}
